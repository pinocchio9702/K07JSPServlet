package model;

public class MyfileDTO {
	private String idx;	//일련번호
	private String name;	//작성자
	private String title;	//제목
	private String inter;	//관심사항
	private String ofile;	//원본파일명
	private String sfile;	//서버에 저장된 파일명
	private String postdate;//작성일
	/**
	 * @return the idx
	 */
	public String getIdx() {
		return idx;
	}
	/**
	 * @param idx the idx to set
	 */
	public void setIdx(String idx) {
		this.idx = idx;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	/**
	 * @return the inter
	 */
	public String getInter() {
		return inter;
	}
	/**
	 * @param inter the inter to set
	 */
	public void setInter(String inter) {
		this.inter = inter;
	}
	/**
	 * @return the ofile
	 */
	public String getOfile() {
		return ofile;
	}
	/**
	 * @param ofile the ofile to set
	 */
	public void setOfile(String ofile) {
		this.ofile = ofile;
	}
	/**
	 * @return the sfile
	 */
	public String getSfile() {
		return sfile;
	}
	/**
	 * @param sfile the sfile to set
	 */
	public void setSfile(String sfile) {
		this.sfile = sfile;
	}
	/**
	 * @return the postdate
	 */
	public String getPostdate() {
		return postdate;
	}
	/**
	 * @param postdate the postdate to set
	 */
	public void setPostdate(String postdate) {
		this.postdate = postdate;
	}
	
	
	
	
	
	
}
